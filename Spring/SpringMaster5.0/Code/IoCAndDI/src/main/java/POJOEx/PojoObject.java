package POJOEx;

/**
 * setter, getter를 가진 가장 기초적인 객체
 */
public class PojoObject {
    String name;
    int age;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
